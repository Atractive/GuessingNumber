package com.mycompany.guessingnumber;


public class BestScore {
    
    private String name;
    private int score;
    
    public BestScore(String n,int sc) {
        this.name=n;
        this.score=sc;
    }
    
    public String getName() {
        return this.name;
    }
    
    public int getScore() {
        return this.score;
    }
    
}
    

